//Declaração da função - Function declaration, posso chamar antes
function soma(x, y){
    return x + y
}

//Expressão de função - Function expression, posso chamar depois
const sub = function(x, y){
    return x - y
}

//Nomeação da expressão de função - Named function expression, posso chamar depois
const mult = function mult(x, y){
    return x * y
}
